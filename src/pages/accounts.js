const useAccounts = (page, rowsPerPage) => {
  return useMemo(
    () => {
      return applyPagination(data, page, rowsPerPage);
    },
    [page, rowsPerPage]
  );
};

const useAccountIds = (accounts) => {
  return useMemo(
    () => {
      return accounts.map((account) => account.id);
    },
    [accounts]
  );
};

